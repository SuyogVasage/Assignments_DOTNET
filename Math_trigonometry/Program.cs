using System;

namespace Math_trigonometry
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int a = Math.Max(4, 5);
            int b = Math.Min(4, 5);
            double c = Math.Pow(3, 2);
            double d = Math.Sqrt(36);
            double e = Math.Round(45.867);
            double f = Math.Sin(Math.PI / 6);
            double g = Math.Cos(Math.PI / 3);
            double h = Math.Tan(Math.PI / 4);
           

            Console.WriteLine(a);
            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
            Console.WriteLine(g);
            Console.WriteLine(h);
        }
    }
    
}
